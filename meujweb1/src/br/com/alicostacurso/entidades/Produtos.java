package br.com.alicostacurso.entidades;

public class Produtos {
	private Integer cod_pr;
	private String nome_pr;
	private String valor;
	
	public Integer getCod_pr() {
		return cod_pr;
	}
	public void setCod_pr(Integer cod_pr) {
		this.cod_pr = cod_pr;
	}
	public String getNome_pr() {
		return nome_pr;
	}
	public void setNome_pr(String nome_pr) {
		this.nome_pr = nome_pr;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	

}
